import { model, Schema } from "mongoose";

const UsuarioSchema = Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  }
  
});

const Usuario = model('Usuario', UsuarioSchema);
export default Usuario;